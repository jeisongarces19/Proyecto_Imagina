function formatDimensionCode(mm) {
  const cm = Math.round(mm / 10);
  return String(cm).padStart(3, '0');
}

function resolveVariantSuffix(variant) {
  if (!variant) return '';
  return `-${variant}`;
}

export function buildKoncisaSurfaceLogicalCode({
  billingWidthMm,
  billingDepthMm,
  shape = 'RECT',
  thicknessMm = 25,
  finishCode = '22008689',
  variant = '',
}) {
  if (!billingWidthMm || !billingDepthMm || !thicknessMm || !finishCode) {
    return null;
  }

  const widthToken = formatDimensionCode(billingWidthMm);
  const depthToken = formatDimensionCode(billingDepthMm);
  const variantSuffix = resolveVariantSuffix(variant);

  return `KONPLUSS${widthToken}${depthToken}${shape}${thicknessMm}-${finishCode}${variantSuffix}`;
}

export function resolveKoncisaSurfaceCodigoPT(params) {
  const logicalCode = buildKoncisaSurfaceLogicalCode(params);

  if (!logicalCode) {
    return {
      logicalCode: null,
      codigoPT: null,
      exists: false,
      rawCodigoPT: null,
    };
  }

  const rawCodigoPT = KONCISA_SURFACE_RULES[logicalCode] || '00000000';

  return {
    logicalCode,
    codigoPT: rawCodigoPT !== '00000000' ? rawCodigoPT : null,
    exists: rawCodigoPT !== '00000000',
    rawCodigoPT,
  };
}
