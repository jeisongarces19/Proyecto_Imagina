function formatDimensionCode(mm) {
  const cm = Math.round(mm / 10);
  return String(cm).padStart(3, '0');
}

// Propuesta nueva: usar espesor real en mm y mapearlo a clave CET
function resolveThicknessToken(thicknessMm) {
  const map = {
    30: '3',
    25: '2',
    18: '18',
    15: '15',
  };

  return map[thicknessMm] || null;
}

export function buildKoncisaSurfaceLogicalCode({
  widthMm,
  depthMm,
  shape = 'RECT',
  thicknessMm = 25,
  finishCode = '22008689',
  variant = '',
}) {
  const widthToken = formatDimensionCode(widthMm);
  const depthToken = formatDimensionCode(depthMm);
  const thicknessToken = resolveThicknessToken(thicknessMm);

  if (!thicknessToken) return null;

  const base = `KONPLUSS${widthToken}${depthToken}${shape}${thicknessToken}-${finishCode}`;
  return variant ? `${base}-${variant}` : base;
}

export function resolveKoncisaSurfaceCodigoPT(params) {
  const logicalCode = buildKoncisaSurfaceLogicalCode(params);
  if (!logicalCode) {
    return {
      logicalCode: null,
      codigoPT: null,
      exists: false,
    };
  }

  const codigoPT = KONCISA_SURFACE_RULES[logicalCode] || '00000000';

  return {
    logicalCode,
    codigoPT: codigoPT !== '00000000' ? codigoPT : null,
    exists: codigoPT !== '00000000',
    rawCodigoPT: codigoPT,
  };
}
